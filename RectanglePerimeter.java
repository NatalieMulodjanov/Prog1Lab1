public class RectanglePerimeter {
  public static void main (String[] args){

    double length;
    double windth;
    double perimeter;
    double area;
    length = 5;
    windth = 5;

    perimeter = 2 * (length + windth);
    area = length * windth;

    System.out.println(" rectangle perimeter  " + perimeter +
                        "\nrectangle area " + area);


  }
}
