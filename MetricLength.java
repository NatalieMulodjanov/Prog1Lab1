public class MetricLength{
  public static void main ( String[] args ){

      final double CM_PER_INCH = 2.54;
      double inches;
      inches = 2;
      double centimeters;

      centimeters = inches * CM_PER_INCH;

      System.out.println(inches + "inches are equivilent to" + centimeters +
      "centimeters" );

  }

}
